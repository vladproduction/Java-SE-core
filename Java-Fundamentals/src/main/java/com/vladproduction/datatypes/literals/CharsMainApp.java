package com.vladproduction.datatypes.literals;

public class CharsMainApp {
    public static void main(String[] args) {
        //big farmer need red tractor
        char big = '\b';
        char farmer = '\f';
        char need = '\n';
        char red = '\r';
        char tractor = '\t';

        System.out.println("big = " + big);
        System.out.println("farmer = " + farmer);
        System.out.println("need (new line) = " + need + "new line");
        System.out.println("red = " + red);
        System.out.println("tractor (tab) = " + tractor + "tractor");
    }
}
